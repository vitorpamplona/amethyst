package com.vitorpamplona.amethyst.ios.util

/**
 * Formats a Unix timestamp (seconds) to a human-readable "time ago" string.
 */
fun Long.toTimeAgo(): String {
    val now =
        com.vitorpamplona.quartz.utils
            .currentTimeSeconds()
    val diff = now - this

    return when {
        diff < 0 -> "just now"
        diff < 60 -> "${diff}s"
        diff < 3600 -> "${diff / 60}m"
        diff < 86400 -> "${diff / 3600}h"
        diff < 604800 -> "${diff / 86400}d"
        diff < 2592000 -> "${diff / 604800}w"
        else -> "${diff / 2592000}mo"
    }
}
